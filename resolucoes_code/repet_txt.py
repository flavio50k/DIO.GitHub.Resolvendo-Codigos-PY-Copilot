# Vamos solicitar como entrada dois números e depois vamos realizar uma operação simples entre eles.# Recebe a string do usuário
texto = input("Digite um texto: ")

# Recebe um número inteiro
vezes = int(input("Digite um número inteiro: "))

# Repete o texto o número de vezes informado
resultado = texto * vezes

# Exibe o resultado
print("Resultado da repetição:", resultado)
